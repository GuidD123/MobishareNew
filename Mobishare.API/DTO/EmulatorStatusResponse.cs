using Mobishare.Core.Enums;

namespace Mobishare.API.DTO
{
    /// <summary>
    /// Risposta per lo status dell'emulatore
    /// </summary>
    public class EmulatorStatusResponse
    {
        /// <summary>
        /// Se l'emulatore è in esecuzione
        /// </summary>
        public bool IsRunning { get; set; }

        /// <summary>
        /// ID del parcheggio gestito (-1 se non avviato)
        /// </summary>
        public int IdParcheggio { get; set; }

        /// <summary>
        /// Lista degli ID dei mezzi attualmente emulati
        /// </summary>
        public List<string> MezziEmulati { get; set; } = [];

        /// <summary>
        /// Timestamp della verifica
        /// </summary>
        public DateTime Timestamp { get; set; } = DateTime.UtcNow;
    }
}
